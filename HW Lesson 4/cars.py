class BMW:
    def max_speed(self):
        print("BMW max speed is 250 km/h")
    def fuel_type(self):
        print("BMW uses petrol")
class Ferrari:
    def max_speed(self):
        print("ferrari max speed is 340 km/h")
    def fuel_type(self):
        print("ferrari uses diesel")
bmw=BMW()
ferrari=Ferrari()
bmw.fuel_type()
bmw.max_speed()
ferrari.fuel_type()
